/* ===== МатЛэнд: фильтр нецензурной лексики =====
   Основан на словаре мата русского языка (nickname76/russian-swears):
   весь русский мат — производные от нескольких корней. Проверяем нормализованную
   строку на вхождение этих корней. Работает и в браузере, и в Node.
*/
(function (root) {
  // корни в НОРМАЛИЗОВАННОМ виде (ё→е, й→и, только строчные кириллические)
  var ROOTS = [
    // хуй и производные
    'хуи', 'хуе', 'хуя', 'хую', 'хуйл', 'хуйн', 'хуяр', 'хуяч', 'хуяк', 'хуиг', 'хуеп', 'хуев', 'хует', 'охуе', 'охуи', 'нахуи', 'похуи', 'дохуа', 'дохуи', 'вхуя', 'вхуи', 'захуя', 'нихуя', 'нихуе', 'подхуи',
    // пизда и производные
    'пизд', 'пезд', 'пизж', 'пиздр', 'спизд', 'распизд', 'въпизд', 'впизд', 'опизд', 'напизд', 'подпизд', 'испизд', 'отпизд',
    // ебать и производные
    'ебал', 'ебан', 'ебат', 'ебаш', 'ебач', 'ебир', 'ебор', 'ебош', 'ебну', 'ебыр', 'ебля', 'ебло', 'еблищ', 'ебуч', 'ебош',
    'взъеб', 'въеб', 'выеб', 'доеб', 'заеб', 'изъеб', 'наеб', 'объеб', 'отъеб', 'переъеб', 'поеб', 'подъеб', 'приеб', 'проеб', 'разъеб', 'съеб', 'уеб', 'усраеб',
    'долбоеб', 'мудоеб', 'пиздоеб', 'распиздяи', 'ебанк', 'ебанат', 'ебанут', 'ебарь', 'заебис', 'заебал', 'наебал', 'наебну', 'наебка', 'ебическ',
    // блядь и производные (без короткого «бля» — оно ловит «корабля», «рубля»)
    'бляд', 'блят', 'бляц', 'бляж', 'бляш', 'блядк', 'бляду', 'выблядок', 'выблядк',
    // муде/мудак и производные (без «муд» — ловит «мудрый», «мудрец»)
    'мудак', 'мудач', 'мудил', 'мудоз', 'мудень', 'мудян', 'муданс', 'блядомуд', 'бляхомуд',
    // прочие обсценные корни
    'залуп', 'елдак', 'елда', 'мандав', 'мандеж', 'мандопро', 'гандон', 'гондон',
    'пидор', 'пидар', 'пидр', 'педрил', 'педор', 'педик', 'гнид',
    'дроч', 'дрочен', 'дрочк', 'дрист', 'ссаки', 'ссан',
    'говн', 'говню', 'дерьм', 'жопа', 'жопу', 'жопо', 'жоп',
    'срать', 'сральн', 'насрат', 'обосра', 'посрат', 'усрат', 'высра', 'засра'
  ];
  // отдельные короткие слова, которые матч только как ЦЕЛОЕ слово-токен
  var WHOLE = ['бля', 'еб', 'ёб', 'хер', 'херня', 'манда'];

  // латиница/цифры, которыми маскируют буквы
  var LOOK = {
    a: 'а', b: 'в', c: 'с', e: 'е', k: 'к', m: 'м', h: 'н', o: 'о', p: 'р', t: 'т', x: 'х', y: 'у', u: 'и', 3: 'з', 0: 'о', 4: 'ч', 6: 'б', 9: 'д'
  };

  function normalize(s) {
    return String(s || '')
      .toLowerCase()
      .split('').map(function (c) { return LOOK[c] || c; }).join('')
      .replace(/ё/g, 'е').replace(/й/g, 'и')
      .replace(/[^а-я]+/g, '')          // убираем всё, кроме кириллицы (склеиваем «х у й»)
      .replace(/(.)\1+/g, '$1');        // «хуууй» → «хуй»
  }
  function normWord(s) {
    return String(s || '').toLowerCase()
      .split('').map(function (c) { return LOOK[c] || c; }).join('')
      .replace(/ё/g, 'е').replace(/й/g, 'и')
      .replace(/[^а-я]/g, '');
  }

  function isProfane(s) {
    var joined = normalize(s);
    for (var i = 0; i < ROOTS.length; i++) {
      var r = ROOTS[i].replace(/ё/g, 'е').replace(/й/g, 'и').replace(/[^а-я]/g, '');
      if (r && joined.indexOf(r) !== -1) return true;
    }
    // проверка отдельных слов-токенов
    var words = String(s || '').toLowerCase().split(/[^a-zа-яё0-9]+/);
    for (var j = 0; j < words.length; j++) {
      var w = normWord(words[j]);
      for (var k = 0; k < WHOLE.length; k++) {
        if (w === normWord(WHOLE[k])) return true;
      }
    }
    return false;
  }

  var api = { isProfane: isProfane, normalize: normalize };
  if (typeof module !== 'undefined' && module.exports) module.exports = api;
  root.BadWords = api;
})(typeof window !== 'undefined' ? window : globalThis);
